
public class Cob {
    private String nama;
    private int nomer;

    public Cob(String nama, int nomer) {
        this.nama = nama;
        this.nomer = nomer;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getNomer() {
        return nomer;
    }

    public void setNomer(int nomer) {
        this.nomer = nomer;
    }



}
